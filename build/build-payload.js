'use strict';
// build/build-payload.js — inline engine/ + dom/ into ONE IIFE string (§7, §3.7).
// The result is safe to prepend to any renderer bundle (and to drop into a userscript):
// it bails when there is no DOM, injects its own CSS, and self-stamps for idempotency.
//
// Run directly to (re)generate dist/. Require it to get buildPayload() for tests.

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
// Order matters: engine primitives first (functions must be declared before dom calls
// them within the shared IIFE scope), then the DOM layer.
const SOURCES = [
  'engine/ranges.js',
  'engine/numbers.js',
  'engine/arrows.js',
  'engine/relations.js',
  'engine/code.js',
  'engine/detect.js',
  'engine/math.js',
  'dom/surfaces.js',
  'dom/apply.js',
];

// patch.sh / userscript check for this marker to stay idempotent (skip already-patched
// bundles). Bump the version suffix when the payload shape changes.
const MARKER = 'claude-rtl-payload-v1';

function read(rel) {
  return fs.readFileSync(path.join(ROOT, rel), 'utf8');
}

// Strip the node-only tail (everything from the __EXPORTS__ sentinel), plus per-file
// 'use strict' and cross-module require() lines — none of which are valid or needed
// once every function shares one IIFE scope.
function stripModule(src) {
  const cut = src.indexOf('// __EXPORTS__');
  if (cut !== -1) src = src.slice(0, cut);
  const kept = src.split('\n').filter((line) => {
    const t = line.trim();
    if (t === "'use strict';" || t === '"use strict";') return false;
    if (t.indexOf('require(') !== -1) return false;
    return true;
  });
  return kept.join('\n').trim();
}

function buildPayload() {
  const css = read('dom/apply.css');
  const parts = SOURCES.map((rel) => {
    let src = stripModule(read(rel));
    if (rel === 'dom/apply.js') {
      // Inline the stylesheet text so the payload is self-contained (no extra fetch).
      src = src.replace("const APPLY_CSS = '__APPLY_CSS__';", () => 'const APPLY_CSS = ' + JSON.stringify(css) + ';');
    }
    return '// ---- ' + rel + ' ----\n' + src;
  });

  return (
    ';(function () {\n' +
    "'use strict';\n" +
    '/* ' + MARKER + ' — generated by build/build-payload.js; do not edit by hand */\n' +
    parts.join('\n\n') +
    '\n})();\n'
  );
}

function writeDist() {
  const payload = buildPayload();
  const distDir = path.join(ROOT, 'dist');
  fs.mkdirSync(distDir, { recursive: true });

  fs.writeFileSync(path.join(distDir, 'payload.js'), payload);

  const template = read('browser/userscript.user.js');
  const userscript = template.replace('/*__PAYLOAD__*/', () => payload);
  fs.writeFileSync(path.join(distDir, 'claude-rtl.user.js'), userscript);

  return { payload, bytes: Buffer.byteLength(payload) };
}

// __EXPORTS__ (build helper is node-only; not part of any payload)
const api = { buildPayload, writeDist, stripModule, MARKER, SOURCES };
if (typeof module !== 'undefined' && module.exports) module.exports = api;

if (require.main === module) {
  const { bytes } = writeDist();
  // eslint-disable-next-line no-console
  console.log('built dist/payload.js + dist/claude-rtl.user.js (' + bytes + ' bytes payload)');
}
